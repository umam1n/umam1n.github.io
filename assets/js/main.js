/* ── Theme ─────────────────────────────────────────── */
const html = document.documentElement;
const themeBtn = document.getElementById('theme-btn');
let dark = localStorage.getItem('theme') === 'dark';

function applyTheme() {
  html.classList.toggle('dark', dark);
  if (themeBtn) themeBtn.innerHTML = dark ? ICONS.sun : ICONS.moon;
}
applyTheme();

themeBtn?.addEventListener('click', () => {
  dark = !dark;
  localStorage.setItem('theme', dark ? 'dark' : 'light');
  applyTheme();
});

/* ── Mobile drawer ──────────────────────────────────── */
const drawer = document.getElementById('mobile-drawer');
const hamburger = document.getElementById('hamburger');
const drawerClose = document.getElementById('drawer-close');

hamburger?.addEventListener('click', () => drawer?.classList.add('open'));
drawerClose?.addEventListener('click', () => drawer?.classList.remove('open'));
drawer?.querySelectorAll('a').forEach(a => a.addEventListener('click', () => drawer.classList.remove('open')));

/* ── Active rail link on scroll ─────────────────────── */
const sections = document.querySelectorAll('section[id]');
const railLinks = document.querySelectorAll('.rail-link');

const io = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      const id = entry.target.id;
      railLinks.forEach(l => l.classList.toggle('active', l.dataset.section === id));
    }
  });
}, { threshold: 0.3 });

sections.forEach(s => io.observe(s));

/* ── Typewriter ─────────────────────────────────────── */
const roles = [
  'Data Visualization Designer',
  'Data Analyst',
  'UI/UX Enthusiast',
  'ML Engineer',
];
let ri = 0, ci = 0, deleting = false;
const tw = document.getElementById('typewriter-text');

function typeLoop() {
  if (!tw) return;
  const word = roles[ri];
  tw.textContent = deleting ? word.slice(0, ci--) : word.slice(0, ci++);
  let delay = deleting ? 50 : 90;
  if (!deleting && ci > word.length) { delay = 1800; deleting = true; }
  else if (deleting && ci < 0) { deleting = false; ri = (ri + 1) % roles.length; ci = 0; delay = 300; }
  setTimeout(typeLoop, delay);
}
typeLoop();

/* ── Fade-up on scroll ───────────────────────────────── */
const fadeEls = document.querySelectorAll('.fade-up');
const fadeObs = new IntersectionObserver((entries) => {
  entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('visible'); fadeObs.unobserve(e.target); } });
}, { threshold: 0.12 });
fadeEls.forEach(el => fadeObs.observe(el));

